CREATE TABLE LISTAS_CIUDAD(
    COD INT, /*ANTES NO ESTABA EN LA TABLA*/
    NOMBRE VARCHAR(50),
    PROVINCIA_ESTADO INT,/*CAMBIO EL NOMBRE Y DE VARCHAR A NUMERIC*/
